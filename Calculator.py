

def add(a, b):
    """This function calculates 
    adding numbers"""

    return a + b

def minus(a, b):
    """This function calculates to subtract 
     second number from first number"""
    
    return a - b

def multiply(a , b):
    """This function calculates to multiply the
    number to the another one"""
    
    return a * b

def divide(a, b):
    """This function divides the number 
    to another number"""
    
    return a / b